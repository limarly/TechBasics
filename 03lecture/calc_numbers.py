def add_numbers(x, y):
    """Function to add two numbers"""
    add_total = x + y
    return add_total

add_total = add_numbers(1,1)
print("The total of the addition is", add_total)

def subtract_numbers(x, y):
    sub_total = x - y
    return sub_total

sub_total = subtract_numbers(5,3)
print("The total of the subtraction is", sub_total)

def multiply_numbers(x, y):
    mul_total = x * y
    return mul_total

mul_total = multiply_numbers(2,2)
print("The total of the multiplication is", mul_total)

def divide_numbers(x, y):
    div_total = x / y
    return div_total

div_total = divide_numbers(3,3)
print("The total of the division is", div_total)